# Создаем пустой словарь для хранения задач
tasks_dict = {}

# Запрашиваем у пользователя дату и задачу трижды
for _ in range(3):
    date = input("Введите дату: ")  # Запрос даты
    task = input("Введите задачу: ")  # Запрос задачи
    tasks_dict[date] = task  # Сохраняем дату как ключ и задачу как значение в словаре

# Выводим данные на экран
print("\nСписок задач:")
for date, task in tasks_dict.items():
    print(f"{date}: {task}")
