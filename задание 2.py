# Создаем пустой список для хранения задач
tasks = []

# Запрашиваем у пользователя дату и задачу трижды
for _ in range(3):
    date = input("Введите дату: ")  # Запрос даты
    task = input("Введите задачу: ")  # Запрос задачи
    tasks.append(f"{date} {task}")  # Добавляем строку с датой и задачей в список

# Выводим результаты построчно
for task in tasks:
    print(task)
